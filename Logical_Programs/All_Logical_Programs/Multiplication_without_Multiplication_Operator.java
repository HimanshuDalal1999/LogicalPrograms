package All_Logical_Programs;

public class Multiplication_without_Multiplication_Operator
{
	public static void main(String[]args)
	{
		int NUM1 = 12;
		int NUM2 = 4;
		
		int SUM = 0;
		
		for(int i=1; i<=NUM2; i++)
		{
			SUM = SUM+NUM1;
		}
		
		System.out.println(SUM);
		
	}

}
