package All_Logical_Programs;

public class Verify_Number_is_Even_or_Odd 
{
	public static void main(String[]args)
	{
		int NUM  = 18;
		
		if(NUM%2==0)
		{
			System.out.println(" Given Number "+NUM+" is EVEN");
		}
		else
		{
			System.out.println(" Given Number "+NUM+" is ODD");
		}
	}

}
